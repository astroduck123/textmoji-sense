// Create index for faster lookups
export const emojiKeywordIndex = new Map<string, string[]>();

emojiDatabase.forEach((data) => {
  data.keywords.forEach((keyword) => {
    const normalized = keyword.toLowerCase();
    if (!emojiKeywordIndex.has(normalized)) {
      emojiKeywordIndex.set(normalized, []);
    }
    emojiKeywordIndex.get(normalized)!.push(data.emoji);
  });
});
